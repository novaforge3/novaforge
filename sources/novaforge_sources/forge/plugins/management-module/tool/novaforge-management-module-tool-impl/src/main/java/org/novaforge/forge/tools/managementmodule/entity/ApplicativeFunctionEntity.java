package org.novaforge.forge.tools.managementmodule.entity;

import org.novaforge.forge.tools.managementmodule.domain.ApplicativeFunction;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import java.io.Serializable;

/**
 * JPA Implementation of an applicative function
 */
@Entity
@Table(name = "applicative_functions")
@NamedQuery(name = "ApplicativeFunctionEntity.findByName", query = "SELECT p FROM ApplicativeFunctionEntity p WHERE p.name = :name")
public class ApplicativeFunctionEntity implements ApplicativeFunction, Serializable
{
	/**
	 * 
	 */
	private static final long serialVersionUID = -3448756519920734070L;

	@Id
	@Column(name = "id", nullable = false, updatable = false)
	@GeneratedValue(strategy = GenerationType.AUTO)
	private Long							id;

	@Column(name = "name", nullable = false, unique = true)
	private String						name;

	@Column(name = "value", nullable = false, unique = false)
	private String						value;

	public Long getId()
	{
		return id;
	}

	@Override
	public String getName()
	{
		return name;
	}

	@Override
	public void setName(final String name)
	{
		this.name = name;
	}

	/**
	 * @return the value
	 */
	@Override
	public String getValue()
	{
		return value;
	}

	/**
	 * @param value
	 *          the value to set
	 */
	@Override
	public void setValue(final String value)
	{
		this.value = value;
	}

	@Override
	public int hashCode()
	{
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(final Object obj)
	{
		if (this == obj)
		{
			return true;
		}
		if (obj == null)
		{
			return false;
		}
		if (getClass() != obj.getClass())
		{
			return false;
		}
		ApplicativeFunctionEntity other = (ApplicativeFunctionEntity) obj;
		if (name == null)
		{
			if (other.name != null)
			{
				return false;
			}
		}
		else if (!name.equals(other.name))
		{
			return false;
		}
		return true;
	}

	@Override
	public String toString()
	{
		return "ApplicativeFunctionEntity [id=" + id + ", name=" + name + ", value=" + value + "]";
	}

}
