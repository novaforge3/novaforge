package org.novaforge.forge.ui.novadeploy.internal.client.exceptions;

public class DescriptorEditorException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -6101170157472710091L;

	public DescriptorEditorException()
	{
		super();
	}

	public DescriptorEditorException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DescriptorEditorException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public DescriptorEditorException(String message)
	{
		super(message);
	}

	public DescriptorEditorException(Throwable cause)
	{
		super(cause);
	}
}
